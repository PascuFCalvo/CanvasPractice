// Colisiones

function detectarColision(dx, dy) {
  // Calcula las coordenadas de la próxima posición de la bola
  const nextX = positionx + dx;
  const nextY = positiony + dy;

  // Comprueba si la próxima posición colisiona con un valor igual a 1 en la matriz
  if (
    (nextY >= 0 || nextY != 0)&& 
    nextY < matrix.length &&
    (nextX >= 0 || nextX != 0) &&
    nextX < matrix[nextY].length &&
    matrix[nextY][nextX] === 1
  ) {
    // Hay una colisión, no permitas que la bola se mueva
    return true;
  }
  if (
    nextY >= 0 &&
    nextY < matrix.length &&
    nextX >= 0 &&
    nextX < matrix[nextY].length &&
    matrix[nextY][nextX] === 2
  ) {
    // Muestra un mensaje de alerta para indicar que has ganado
    alert("¡Has ganado!");
  }

  return false; // No hay colisión, la bola puede moverse
}

