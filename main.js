// esto dibuja la bola y el tablero

function draw() {
  context.fillStyle = "lightblue";
  context.fillRect(0, 0, canvas.width, canvas.height);

  matrix.forEach((row, y) => {
    row.forEach((value, x) => {
      if (value === 1) {
        context.fillStyle = "brown";
        context.fillRect(x, y, 1, 1);
      } else if (value === 2 || value === 4) {
        context.fillStyle = "green";
        context.fillRect(x, y, 1, 1);
      }
    });
  });

  bola.forma.forEach((bola) => {
    if (bola === 3) {
      context.fillStyle = "black";
      context.fillRect(positionx, positiony, 1, 1);
    }
  });

  
}
//movimiento

document.addEventListener("keydown", (e) => {
  let dx = 0;
  let dy = 0;

  if (e.key === "ArrowRight") {
    dx = 1;
  }
  if (e.key === "ArrowLeft") {
    dx = -1;
  }
  if (e.key === "ArrowDown") {
    dy = 1;
  }
  if (e.key === "ArrowUp") {
    dy = -1;
  }

  // Antes de mover la bola, verifica la colisión
  if (!detectarColision(dx, dy)) {
    positionx += dx;
    positiony += dy;
  }
});

